package com.pidkui;

public class Test009 {
    public static void main(String[] args) {

        int a = 10;
        int b = -10;

        System.out.println("a << 1 :" + (a << 1));
        System.out.println("a >> 1 :" + (a >> 1));
        System.out.println("a >>> 1 :" + (a >>> 1));
        System.out.println("b << 1 :" + (b << 1));
        System.out.println("b >> 1 :" + (b >> 1));
        System.out.println("b >>> 1 :" + (b >>> 1));
    }
}
